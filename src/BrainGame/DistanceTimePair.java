package BrainGame;


public class DistanceTimePair {

    private final int distance;
    private final int time;

    public DistanceTimePair(int distance, int time) {
        this.distance = distance;
        this.time = time;
    }

    public int getDistance() {
        return distance;
    }

    public int getTime() {
        return time;
    }

}

